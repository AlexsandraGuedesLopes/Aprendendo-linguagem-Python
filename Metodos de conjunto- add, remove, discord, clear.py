frutas={"maçã", "banana", "laranja"}
frutas.remove("banana")  # Remove um elemento da tupla
print(frutas)  # Imprime a tupla atualizada
frutas.discard("uva")  # Remove um elemento da tupla, se existir
print(frutas)  # Imprime a tupla atualizada
frutas.clear()
print(frutas)  # Limpa todos os elementos da tupla


